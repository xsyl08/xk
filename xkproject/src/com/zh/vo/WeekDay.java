package com.zh.vo;

public class WeekDay {
	private Course monday;
	private Course tuesday;
	private Course wendsday;
	private Course thursday;
	private Course friday;
	private Course saturday;
	private Course sunday;
	public Course getMonday() {
		return monday;
	}
	public void setMonday(Course monday) {
		this.monday = monday;
	}
	public Course getTuesday() {
		return tuesday;
	}
	public void setTuesday(Course tuesday) {
		this.tuesday = tuesday;
	}
	public Course getWendsday() {
		return wendsday;
	}
	public void setWendsday(Course wendsday) {
		this.wendsday = wendsday;
	}
	public Course getThursday() {
		return thursday;
	}
	public void setThursday(Course thursday) {
		this.thursday = thursday;
	}
	public Course getFriday() {
		return friday;
	}
	public void setFriday(Course friday) {
		this.friday = friday;
	}
	public Course getSaturday() {
		return saturday;
	}
	public void setSaturday(Course saturday) {
		this.saturday = saturday;
	}
	public Course getSunday() {
		return sunday;
	}
	public void setSunday(Course sunday) {
		this.sunday = sunday;
	}
}
